# Operadores aritméticos
#(+) Suma (-) Resta (*) Multiplicación (/) División (%) Modulo o Residuo
#(//) Division con valor entero (**) Exponenciación

print(10+10)
print(10-5)
print(10*2)
print(10/2)
print(10%2) #Presenta el residuo de la división 
print(10//3) #El uso del operador // presenta unicamente el valor entero de la división
print(2**3) #El uso del operador ** presenta la potencia de un número
print(2**3+3-7/1//4) #El orden de las operaciones es el siguiente: potencias, multiplicaciones, división, sumas y restas.




