# >: El comparador > es el operador de mayor que.
print("Pruebas >")
print(7 > 3)
print(3 > 7)
print(7 > 7)

# <: El comparador < es el operador de menor que.
print("Pruebas <")
print(5 < 6)
print(6 < 5)
print(5 < 5)

# >=: El operador >= es el operador de mayor o igual que.
print("Pruebas >=")
print(2 >= 1)
print(2 >= 3)
print(2 >= 2)

#De la misma forma, el operador <= es el operador de menor o igual que.

# ==: El operador == es el operador de igualdad.
print("Pruebas ==")
print(6 == 6)
print(6==5)

#!=: El operador != es el operador de desigualdad.
print("Pruebas !=")
print(6 != 10)
print(6 != 6)

